# Ahoy matey! This fine vessel charts the course for generating numbers 1 to 10!
# Avast! No treasure map needed — just a simple loop to plunder all numbers in sight.


def generate_numbers():
    """
    Generate and print numbers from 1 to 10.
    Every number be a piece of gold, and we shall collect them all!
    """
    # Arrr! Set sail from 1 and anchor at 10 — inclusive waters ahead!
    for number in range(1, 11):
        # Each number be a doubloon — print it proud like a true buccaneer!
        print(number)


if __name__ == "__main__":
    # Hoist the Jolly Roger and run the main voyage!
    generate_numbers()
